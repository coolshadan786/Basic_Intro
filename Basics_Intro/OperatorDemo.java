
public class OperatorDemo {
	/*
	 * Arithmetic + - * /
	 * logical
	 * Bitwise
	 * Relational
	 * 
	 */
	public static void main(String[] args) {
		int m=10,n=5;
		int a1=m+n;
		int a2=m-n;
		int a3=m*n;
		int a4=m/n;
		int a5=m%n;
		
		System.out.println(a1);
		System.out.println(a2);
		System.out.println(a3);
		System.out.println(a4);
		System.out.println(a5);

	}

}
